#string
username = "saad24"
print(username)
print(type(username))

#numbers:

#integer
randNum = 12
print(randNum)
print(type(randNum))

#float 
floatNumber = 12.2
print(floatNumber)
print(type(floatNumber))

#list
randomList = [2,5,1,5,"Saad"]
print(type(randomList))

#tuple (tuple cannot be changed)
randomTuple = (12,151,51,"Mohammad Saad Shahid")
print(type(randomTuple))

#dictionary
randDict = {"bookName": "Quran", "surah": 114, "longest": "Baqarah", "mushaf": "Madani"}

print(randDict)
print(type(randDict))

#sets (there can be no repeated values (the repeated values will not be shown))
randSet = {"Mohammad", "Saad", "Saad", "Saad", "Shahid", "Sahal"} #when this is printed, the repeated things will not appear
print(randSet)
print(type(randSet))